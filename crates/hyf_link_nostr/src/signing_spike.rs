#[cfg(test)]
mod tests {
    use k256::schnorr::{Signature, SigningKey, VerifyingKey};

    #[test]
    fn k256_schnorr_signs_and_verifies_bip340_vector() -> Result<(), k256::schnorr::Error> {
        let mut secret_key = [0; 32];
        secret_key[31] = 3;
        let public_key = [
            0xf9, 0x30, 0x8a, 0x01, 0x92, 0x58, 0xc3, 0x10, 0x49, 0x34, 0x4f, 0x85, 0xf8, 0x9d,
            0x52, 0x29, 0xb5, 0x31, 0xc8, 0x45, 0x83, 0x6f, 0x99, 0xb0, 0x86, 0x01, 0xf1, 0x13,
            0xbc, 0xe0, 0x36, 0xf9,
        ];
        let message = [0; 32];
        let aux_rand = [0; 32];
        let signature = [
            0xe9, 0x07, 0x83, 0x1f, 0x80, 0x84, 0x8d, 0x10, 0x69, 0xa5, 0x37, 0x1b, 0x40, 0x24,
            0x10, 0x36, 0x4b, 0xdf, 0x1c, 0x5f, 0x83, 0x07, 0xb0, 0x08, 0x4c, 0x55, 0xf1, 0xce,
            0x2d, 0xca, 0x82, 0x15, 0x25, 0xf6, 0x6a, 0x4a, 0x85, 0xea, 0x8b, 0x71, 0xe4, 0x82,
            0xa7, 0x4f, 0x38, 0x2d, 0x2c, 0xe5, 0xeb, 0xee, 0xe8, 0xfd, 0xb2, 0x17, 0x2f, 0x47,
            0x7d, 0xf4, 0x90, 0x0d, 0x31, 0x05, 0x36, 0xc0,
        ];

        let signing_key = SigningKey::from_slice(&secret_key)?;
        assert_eq!(
            signing_key.verifying_key().to_bytes().as_slice(),
            public_key
        );

        let signed = signing_key.sign_raw(&message, &aux_rand)?;
        assert_eq!(signed.to_bytes(), signature);

        let verifying_key = VerifyingKey::from_slice(&public_key)?;
        let parsed = Signature::from_slice(&signature)?;
        verifying_key.verify_raw(&message, &parsed)
    }
}
